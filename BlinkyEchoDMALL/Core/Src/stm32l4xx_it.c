/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
	  /* USER CODE BEGIN USART1_IRQn 0 */
		if (LL_USART_IsActiveFlag_RXNE(USART1))
		    {
			if(LL_USART_IsActiveFlag_TC(USART1))
			{

				rx_buf = LL_USART_ReceiveData8(USART1);
				if(rx_buf=='\r'||counter>=BUF_SIZE){
					LL_DMA_DisableChannel(DMA1, LL_DMA_CHANNEL_2);
					LL_DMA_ConfigAddresses(DMA1, LL_DMA_CHANNEL_2, (uint32_t)full_buf, LL_USART_DMA_GetRegAddr(USART1,LL_USART_DMA_REG_DATA_TRANSMIT), LL_DMA_DIRECTION_MEMORY_TO_PERIPH);

					LL_DMA_SetDataLength(DMA1,LL_DMA_CHANNEL_2 , counter);
					LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_2);
					LL_USART_EnableDMAReq_TX(USART1);
					counter = 0;
				}
				else{
				        full_buf[counter] = rx_buf;
				        counter++;
				        LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_2);}
			}
		    }

  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

